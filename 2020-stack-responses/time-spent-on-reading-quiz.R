times_detail <- times %>%
  mutate(
    hours = as.numeric(sub("[^\\d]*?(\\d+) hour.*", "\\1", time_taken)),
    minutes = as.numeric(sub("[^\\d]*?(\\d+) min.*", "\\1", time_taken)),
    seconds = as.numeric(sub("[^\\d]*?(\\d+) sec.*", "\\1", time_taken))
  ) %>% 
  mutate(across(where(is.numeric), replace_na, 0))%>%
  mutate(
    total_time = seconds + 60*minutes + 60*60*hours
  )

times_detail %>% 
  summarise(
    mean_time_min = mean(total_time)/60,
    sd_time_min = sd(total_time/60),
    median_time_min = median(total_time)/60,
  )

